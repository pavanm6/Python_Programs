#9 WAP to accept week_number and print weekday
#    1 -- monday
 #   2. tuesday
  #  7 sunday
n=int(input("Enter a number"))
if n==1:
    print("Monday")
elif n==2:
    print("tuesday")
elif n==3:
    print("Wednesday")
elif n==4:
    print("thursday")
elif n==5:
    print("friday")
elif n==6:
    print("saturday")
elif n==7:
    print("sunday")
else:
    print("INVALID")
